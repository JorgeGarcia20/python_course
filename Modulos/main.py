import calculator
"""Importamos el modulo calculator para tener mas ordenado el codigo"""
result = calculator.sum(30,45)
print('El resultado de la suma es:', result)
result = calculator.res(30,45)
print('El resultado de la resta es:', result)
result = calculator.mult(30,45)
print('El resultado de la multiplicacion es:', result)
result = calculator.div(30,45)
print('El resultado de la divicion es:', result)