def sum(x, y):
    """Recibe dos valores y retorna la suma de los dos valores"""
    return x + y

def res(x, y):
    """Recibe dos valores y retorna la resta de los dos valores"""
    return x - y

def mult(x, y):
    """Recibe dos valores y retorna la multiplicacion de los dos valores"""
    return x * y

def div(x, y):
    """Recibe dos valores y retorna la divicion de los dos valores"""
    return x / y