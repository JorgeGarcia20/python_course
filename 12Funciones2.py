def suma(valor_uno, valor_dos, valor_tres):
    return valor_uno + valor_dos + valor_tres

def multiplicacion(valor_uno, valor_dos):
    return valor_uno * valor_dos

def resta(valor_uno, valor_dos):
    return valor_uno - valor_dos

def divicion(valor_uno, valor_dos):
    return valor_uno / valor_dos

resultado = suma(30, 20, 10)
print(resultado)

resultado = resta(30, 20)
print(resultado)

resultado = multiplicacion(30, 20)
print(resultado)

resultado = divicion(30, 20)
print(resultado)
